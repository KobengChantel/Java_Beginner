package Book4.chapter2;

public class Days {
    public static void main(String[] args) {
        String[] days = {
                "Sunday", "Monday", "Tuesday",
                "Wednesday", "Thursday",
                "Friday", "Saturday"
        };

        // Loop through the days array and print each day
        for (String day : days) {
            System.out.println(day);
        }
    }
}
